luas = float(input("Masukkan luas segitga : "))
keliling = float(input("Masukkan keliling segitga : "))


a = float(input("Masukkan sisi a : "))
b = float(input("Masukkan sisi b : "))
c = float(input("Masukkan sisi c : "))

luasSegitiga = (1/2) * luas * keliling
kelilingSegitiga = a + b + c
print("Luas Segitiga = %.0f Cm" % luasSegitiga)
print("Keliling Segitiga = %.0f Cm" % kelilingSegitiga)